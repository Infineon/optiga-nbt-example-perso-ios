import SwiftUI

/// Define an extension on the ``Color`` class
public extension Color {
    /// A static property that represents the color ``ocean500``.
    static let ocean500 = Color("Ocean-500")

    /// A static property that represents the color ``engineering100`.
    static let engineering100 = Color("Engineering-100")

    /// A static property that represents the color ``engineering200`.
    static let engineering200 = Color("Engineering-200")

    /// A static property that represents the color ``engineering300`.
    static let engineering300 = Color("Engineering-300")

    /// A static property that represents the color ``background``.
    static let background = Color("Background")

    /// A static property that represents the color ``baseBlack``.
    static let baseBlack = Color("BaseBlack")
}
