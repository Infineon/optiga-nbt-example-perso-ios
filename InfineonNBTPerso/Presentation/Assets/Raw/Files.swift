/// Files class provides the static constant representing raw files
public enum Files {
    /// The name of the sample device certificate pen file.
    ///
    /// The `sampleDeviceCertificate` constant represents the sample device certificate pen file.
    public static let sampleDeviceCertificate = "SampleDeviceCertificate"

    /// The name of the sample device key pen file.
    ///
    ///  The `sampleDeviceKey` constant represents the sample device key pen file.
    public static let sampleDeviceKey = "SampleDeviceKey"

    /// The extension/file type of the sample device certificate pen file.
    ///
    /// The `SAMPLE_DEVICE_CERT` constant represents the sample device certificate pen file
    /// extension/file type.
    public static let sampleDeviceKeyFileType = "pem"
}
